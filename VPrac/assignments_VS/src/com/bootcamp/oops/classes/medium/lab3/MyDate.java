package com.bootcamp.oops.classes.medium.lab3;

/**
 * 
 * @author Boot Camp User 011 This class has properties related to day, month
 *         and year.
 */
public class MyDate {

	private int month;
	private int day;
	private int year;

	public MyDate() {

	}

	public int getMonth() {
		return month;
	}

	public void setMonth(int month) {
		this.month = month;
	}

	public int getDay() {
		return day;
	}

	public void setDay(int day) {
		this.day = day;
	}

	public int getYear() {
		return year;
	}

	public void setYear(int year) {
		this.year = year;
	}

}
