package com.bootcamp.operators.lab3;

/**
 * 
 * @author vishal, This class displays two number and perform / operation to
 *         produce quotient and % operation to produce remainder
 *
 */
public class QuotientAndReminderCaluculator {
	public static void main(String[] args) {
		int number = 25;
		int number1 = 30;
		System.out.println("Two numbers are  " + number + " and " + number1);
		System.out.println(number + "/" + number1 + " = " + number / number1 + "\t" + number + "%" + number1 + " = "
				+ number % number1);

	}
}
