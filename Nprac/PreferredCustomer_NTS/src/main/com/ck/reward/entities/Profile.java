package com.ck.reward.entities;

public class Profile {
  private String gender;
  private String maritalStatus;
  private String birthDay;
  private String birthMonth;
  private String incomeRange;
  private String phone;
  private String[] emailPreferences;
  private Address address;
  
  
  
/**
 * @return the gender
 */
public String getGender() {
	return gender;
}
/**
 * @param gender the gender to set
 */
public void setGender(String gender) {
	this.gender = gender;
}
/**
 * @return the maritalStatus
 */
public String getMaritalStatus() {
	return maritalStatus;
}
/**
 * @param maritalStatus the maritalStatus to set
 */
public void setMaritalStatus(String maritalStatus) {
	this.maritalStatus = maritalStatus;
}
/**
 * @return the birthDay
 */
public String getBirthDay() {
	return birthDay;
}
/**
 * @param birthDay the birthDay to set
 */
public void setBirthDay(String birthDay) {
	this.birthDay = birthDay;
}
/**
 * @return the birthMonth
 */
public String getBirthMonth() {
	return birthMonth;
}
/**
 * @param birthMonth the birthMonth to set
 */
public void setBirthMonth(String birthMonth) {
	this.birthMonth = birthMonth;
}
/**
 * @return the incomeRange
 */
public String getIncomeRange() {
	return incomeRange;
}
/**
 * @param incomeRange the incomeRange to set
 */
public void setIncomeRange(String incomeRange) {
	this.incomeRange = incomeRange;
}
/**
 * @return the phone
 */
public String getPhone() {
	return phone;
}
/**
 * @param phone the phone to set
 */
public void setPhone(String phone) {
	this.phone = phone;
}
/**
 * @return the emailPreferences
 */
public String[] getEmailPreferences() {
	return emailPreferences;
}
/**
 * @param emailPreferences the emailPreferences to set
 */
public void setEmailPreferences(String[] emailPreferences) {
	this.emailPreferences = emailPreferences;
}
/**
 * @return the address
 */
public Address getAddress() {
	return address;
}
/**
 * @param address the address to set
 */
public void setAddress(Address address) {
	this.address = address;
}
  
  

	
	
}
